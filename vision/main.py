MODEL_PATH        = "yolo11n.pt"
CONFIDENCE_THRESH = 0.40
DETECT_PEOPLE     = False         # Set to True to also detect people

# SO-100 robot camera resolution (standard USB camera)
ROBOT_CAM_WIDTH   = 640
ROBOT_CAM_HEIGHT  = 480

# Software zoom — crop factor (1.0 = no zoom, 2.0 = 2x zoom, etc.)
# Change at runtime with '+' / '-' keys, or set the default here
ZOOM_DEFAULT      = 1.0
ZOOM_MIN          = 1.0
ZOOM_MAX          = 5.0
ZOOM_STEP         = 0.25

# Depth estimation (pinhole approximation)
KNOWN_BOOK_WIDTH_M = 0.21  # real-world book width in metres
FOCAL_LENGTH_PX    = 600.0 # fallback — overridden by camera_calibration.json if present

# Calibration files (generated by calibrate_camera.py / calibrate_workspace.py)
# Paths are resolved relative to this script's directory so the script works
