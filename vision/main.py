def detect_aruco_markers(frame):
    aruco_dict = cv2.aruco.getPredefinedDictionary(cv2.aruco.DICT_4X4_50)
    parameters = cv2.aruco.DetectorParameters()
    detector = cv2.aruco.ArucoDetector(aruco_dict, parameters)
    corners, ids, _ = detector.detectMarkers(frame)
    return corners, ids

def is_inside_workspace(px, py, workspace_polygon):
    """Check if pixel (px, py) is inside the workspace polygon."""
    return cv2.pointPolygonTest(workspace_polygon, (float(px), float(py)), False) >= 0

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--webcam", action="store_true",
                        help="Force laptop webcam — use this to test YOLO without the robot connected")
    parser.add_argument("--camera", type=int, default=None,
                        help="Manually specify a camera index")
    parser.add_argument("--detect-people", action="store_true",
                        help="Also detect people in addition to books")
    args = parser.parse_args()

    # Build target classes based on flags
    target_classes = ["book", "bottle"]

    # ── Model ─────────────────────────────────────────────────────────────────
    # (load early so we can resolve class name → YOLO class ID)
    print(f"[INFO] Loading {MODEL_PATH} ...")
    model = YOLO(MODEL_PATH)
    print("[INFO] Model ready.")

    # Map target class names to YOLO numeric IDs so we can filter at inference
    yolo_class_ids = [
        cid for cid, name in model.names.items()
        if name in target_classes
    ]
    print(f"[INFO] Filtering for classes: {target_classes}  (YOLO IDs: {yolo_class_ids})")

    # ── Pick camera ───────────────────────────────────────────────────────────
    if args.camera is not None:
        cam_index  = args.camera
        robot_mode = not args.webcam

    elif args.webcam:
        cam_index  = 0
        robot_mode = False
        print("[CAM] Webcam mode forced — robot camera scan skipped.")

    else:
        robot_cam = find_robot_camera()
        if robot_cam is not None:
            cam_index  = robot_cam
            robot_mode = True
        else:
            cam_index  = 0
            robot_mode = False
            print("[CAM] Falling back to webcam at index 0.")

    cap = open_camera(cam_index, robot_mode)
    w   = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    h   = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    # ── Load calibrations ─────────────────────────────────────────────────────
    cam_matrix, dist_coeffs, focal_px = load_camera_calibration()
    ws_polygon, ws_homography = load_workspace_calibration()

    # Software zoom level (change at runtime with + / - keys)
    zoom = ZOOM_DEFAULT

    mode_label = "ROBOT CAM" if robot_mode else "WEBCAM (test)"
    print(f"\n[INFO] Running — {mode_label} — press 'q' to quit.\n")

    try:
        while True:
            ret, frame = cap.read()
            if not ret:
                print("[WARN] Frame grab failed — retrying ...")
                time.sleep(0.05)
                continue

            # ── Software zoom (centre crop + upscale) ─────────────────────────
            frame = crop_zoom(frame, zoom)
            # ── Undistort if calibration available ───────────────────────
            if cam_matrix is not None:
                frame = cv2.undistort(frame, cam_matrix, dist_coeffs)
            # ── YOLO inference ────────────────────────────────────────────────
            results = model.predict(
                source=frame,
                imgsz=max(w, h),
                conf=CONFIDENCE_THRESH,
                classes=yolo_class_ids,
                verbose=False,
            )

            best_box  = None
            best_conf = 0.0
            best_class = None

            for result in results:
                for box in result.boxes:
                    class_name = model.names[int(box.cls)]
                    if class_name in target_classes:
                        conf = float(box.conf)
                        if conf > best_conf:
                            best_conf = conf
                            best_box  = box.xyxy[0].cpu().numpy()
                            best_class = class_name

            # ── Detection result ──────────────────────────────────────────────
            if best_box is not None:
                x1, y1, x2, y2  = best_box
                depth_m          = estimate_depth_m(x2 - x1, focal_px)
                cx_norm, cy_norm = bbox_centre_normalised(x1, y1, x2, y2, w, h)
                x = cx_norm
                y = cy_norm
                z = depth_m

                # Check workspace boundary using saved calibration
                obj_cx = int((x1 + x2) / 2)
                obj_cy = int((y1 + y2) / 2)
                if ws_polygon is not None:
                    inside = is_inside_workspace(obj_cx, obj_cy, ws_polygon)
                else:
                    inside = True   # no workspace calibration → accept all

                if inside:
                    print(f"[DETECT] {best_class.upper()} found  conf={best_conf:.2f}  "
                          f"coords=({x:+.2f}, {y:+.2f}, {z:.2f}m)")
                    send_to_arm(x, y, z)

            # ── Display ───────────────────────────────────────────────────────
            display = frame.copy()

            # Draw workspace boundary (cyan outline)
            if ws_polygon is not None:
                cv2.polylines(display, [ws_polygon], isClosed=True,
                              color=(255, 200, 0), thickness=2)

            if best_box is not None:
                box_color  = (0, 255, 0) if inside else (0, 0, 255)
                label_text = f"{best_class} {best_conf:.2f} | {depth_m:.2f}m"
                if not inside:
                    label_text += " [OUTSIDE]"

                cv2.rectangle(display,
                              (int(x1), int(y1)),
                              (int(x2), int(y2)),
                              box_color, 2)
                cv2.putText(display, label_text,
                            (int(x1), max(int(y1) - 8, 12)),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.5, box_color, 1)

            # Mode / zoom label top-left
            cv2.putText(display, f"{mode_label}  zoom {zoom:.2f}x", (6, 18),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.45,
                        (0, 200, 255) if robot_mode else (100, 100, 255), 1)

            cv2.imshow("Book Detection", display)
            key = cv2.waitKey(1) & 0xFF
            if key == ord("q"):
                break
            elif key in (ord("+"), ord("=")):   # '+' or '=' to zoom in
                zoom = min(zoom + ZOOM_STEP, ZOOM_MAX)
                print(f"[ZOOM] {zoom:.2f}x")
            elif key == ord("-"):                 # '-' to zoom out
                zoom = max(zoom - ZOOM_STEP, ZOOM_MIN)
                print(f"[ZOOM] {zoom:.2f}x")

    finally:
        cap.release()
        cv2.destroyAllWindows()
        print("[INFO] Done.")


if __name__ == "__main__":
    main()