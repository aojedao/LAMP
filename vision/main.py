CAMERA_CALIB_FILE    = os.path.join(_SCRIPT_DIR, "camera_calibration.json")
WORKSPACE_CALIB_FILE = os.path.join(_SCRIPT_DIR, "workspace_calibration.json")


# ══════════════════════════════════════════════════════════════════════════════
#  CALIBRATION LOADERS
# ══════════════════════════════════════════════════════════════════════════════

def load_camera_calibration():
    """Load camera intrinsics. Returns (cam_matrix, dist_coeffs, fx) or Nones."""
    if not os.path.exists(CAMERA_CALIB_FILE):
        print(f"[CALIB] {CAMERA_CALIB_FILE} not found — using defaults.")
        return None, None, FOCAL_LENGTH_PX
    with open(CAMERA_CALIB_FILE) as f:
        data = json.load(f)
    cam_matrix  = np.array(data["camera_matrix"], dtype=np.float64)
    dist_coeffs = np.array(data["dist_coeffs"], dtype=np.float64)
    fx = cam_matrix[0, 0]
    print(f"[CALIB] Camera intrinsics loaded — fx={fx:.1f} px")
    return cam_matrix, dist_coeffs, fx


def load_workspace_calibration():
    """Load workspace polygon (pixel corners). Returns np array or None."""
    if not os.path.exists(WORKSPACE_CALIB_FILE):
        print(f"[CALIB] {WORKSPACE_CALIB_FILE} not found — workspace filter DISABLED.")
        return None, None
    with open(WORKSPACE_CALIB_FILE) as f:
        data = json.load(f)
    pixel_corners = np.array(data["pixel_corners"], dtype=np.int32)
    homography    = np.array(data["homography"],    dtype=np.float64)
    ws = data["workspace_m"]
    print(f"[CALIB] Workspace loaded — {ws[0]*100:.0f}cm × {ws[1]*100:.0f}cm  "
          f"({len(pixel_corners)} corner polygon)")
    return pixel_corners, homography


# ══════════════════════════════════════════════════════════════════════════════
#  CAMERA SETUP
#  Tries to find the robot camera first, falls back to webcam
# ══════════════════════════════════════════════════════════════════════════════

def find_robot_camera():
    """
    Scans camera indices 0-5 and returns the first one that can be
    opened. Prefers indices > 0 (external USB cam) over the built-in
    webcam at index 0.  Returns None if nothing is found.
    """
    print("[CAM] Scanning for robot camera ...")
    for idx in range(1, 6):          # try external cameras first
        cap = cv2.VideoCapture(idx)
        if not cap.isOpened():
            cap.release()
            continue
        cap.set(cv2.CAP_PROP_FRAME_WIDTH,  ROBOT_CAM_WIDTH)
        cap.set(cv2.CAP_PROP_FRAME_HEIGHT, ROBOT_CAM_HEIGHT)
        w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
        h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
        cap.release()
        print(f"[CAM] Robot camera found at index {idx} ({w}x{h})")
        return idx
    print("[CAM] Robot camera not found.")
    return None


def open_camera(index, robot_mode):
    cap = cv2.VideoCapture(index)
    if not cap.isOpened():
        raise RuntimeError(f"Cannot open camera at index {index}.")
    if robot_mode:
        cap.set(cv2.CAP_PROP_FRAME_WIDTH,  ROBOT_CAM_WIDTH)
        cap.set(cv2.CAP_PROP_FRAME_HEIGHT, ROBOT_CAM_HEIGHT)
        w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
        h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
        print(f"[CAM] Robot camera opened — {w}x{h}")
    else:
        w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
        h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
        print(f"[CAM] Webcam opened — {w}x{h}  (YOLO test mode)")
    return cap


# ══════════════════════════════════════════════════════════════════════════════
#  VISION HELPERS
# ══════════════════════════════════════════════════════════════════════════════

def crop_zoom(frame, zoom):
    """Return a centre-cropped and re-scaled version of *frame*."""
    if zoom <= 1.0:
        return frame
    h, w = frame.shape[:2]
    crop_w = int(w / zoom)
    crop_h = int(h / zoom)
    x0 = (w - crop_w) // 2
    y0 = (h - crop_h) // 2
    cropped = frame[y0:y0 + crop_h, x0:x0 + crop_w]
    return cv2.resize(cropped, (w, h), interpolation=cv2.INTER_LINEAR)


def estimate_depth_m(bbox_width_px, focal_length=None):
    if bbox_width_px < 1:
        return float("inf")
    fl = focal_length if focal_length is not None else FOCAL_LENGTH_PX
    return (KNOWN_BOOK_WIDTH_M * fl) / bbox_width_px


def bbox_centre_normalised(x1, y1, x2, y2, fw, fh):
    """Returns (cx, cy) in [-1, 1] with (0, 0) at frame centre."""
    cx = ((x1 + x2) / 2) / fw * 2 - 1
    cy = ((y1 + y2) / 2) / fh * 2 - 1
    return cx, cy


